def make_graph_from_blocks(orig):
    '''Taking partition from Pavlovic14a.  This reorganizes the adjacency list
    in terms of blocks.'''
    temp = networkx.Graph()

    for block in sorted(blocks):
        for node in blocks[block]:
            temp.add_node(node)

    for f in orig.edge:
        for t in orig.edge[f]:
            temp.add_edge(f,t)

    return temp

